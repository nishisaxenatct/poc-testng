package com.states.texas;

public class Calculator {
	
	public int addition(int a, int b){
		
		int add = a+b;
		return add;
		
	}
	
	public int substraction(int a, int b){
		
		int substract = a-b;
		return substract;
	}
	
	public int multiplication(int a, int b){
		int multiply = a*b;
		return multiply;
	}
	
	public int division(int a, int b){
		int divide = a/b;
		return divide;
	}

}
